// Check for Match
bool is_match(const uint8_t scanned[7], const uint8_t list[][7], int count) {
    for (int i = 0; i < count; i++) {
        if (memcmp(scanned, list[i], 7) == 0) {
            return true;
        }
    }
    return false;
}

// Get Color
int identify_tag_color(const uint8_t scanned[7], 
                       const uint8_t green[][7], int g_count,
                       const uint8_t yellow[][7], int y_count,
                       const uint8_t purple[][7], int p_count) 
{
    if (is_match(scanned, green, g_count))   return 1;
    if (is_match(scanned, yellow, y_count))  return 2;
    if (is_match(scanned, purple, p_count))  return 3;
    
    return 0; // None found
}

